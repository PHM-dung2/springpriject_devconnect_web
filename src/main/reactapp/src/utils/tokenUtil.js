// - 모든 컴포넌트, API, 라우터에서 공통으로 사용
// - 키값은 고정: 'accessToken'
// =======================================================================================

const TOKEN_KEY = 'accessToken'; // ✅ 모든 모듈에서 동일한 키 사용

// =======================================================================================
// ✅ 1. 토큰 저장
/*
    - 로그인 성공 시 JWT 토큰을 저장
    - 기존 토큰이 있을 경우 덮어씀
*/
export const saveToken = (token) => {
    localStorage.setItem(TOKEN_KEY, token);
};

// =======================================================================================
// ✅ 2. 토큰 조회
/*
    - API 요청, 라우터 접근 등에서 토큰 필요 시 사용
*/
export const getToken = () => {
    return localStorage.getItem(TOKEN_KEY);
};

// =======================================================================================
// ✅ 3. 토큰 삭제
/*
    - 로그아웃 시 or 만료 시 강제 삭제
*/
export const removeToken = () => {
    localStorage.removeItem(TOKEN_KEY);
};